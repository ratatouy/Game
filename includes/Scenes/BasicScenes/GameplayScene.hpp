    ////////////////////////////////////////////////////////////
    GameplayScene(sf::Vector2i origin, sf::Vector2u size) : Scene(origin, size) {};

    ////////////////////////////////////////////////////////////
    /// \brief Update the scene
    /// 
    /// Order of updates :
    /// \li Entities
    ////////////////////////////////////////////////////////////
    void update () override;

    ////////////////////////////////////////////////////////////
    /// \brief Updates all of the entities
    ////////////////////////////////////////////////////////////
    void updateEntities();


private:
