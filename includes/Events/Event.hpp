#ifndef EVENT_HPP
#define EVENT_HPP

////////////////////////////////////////////////////////////
/// \brief Abstract class for all events
/// 
/// Events are signals that can be of any kind, sent by any object,
/// and dispatched to any object.
////////////////////////////////////////////////////////////
class Event
{
public:
    ////////////////////////////////////////////////////////////
    /// \brief Default constructor
    ////////////////////////////////////////////////////////////
    Event() {};

    ////////////////////////////////////////////////////////////
    /// \brief Virtual destructor
    ////////////////////////////////////////////////////////////
    virtual ~Event() {};
};

#endif